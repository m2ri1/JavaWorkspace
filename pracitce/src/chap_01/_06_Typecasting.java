package chap_01;

public class _06_Typecasting {
    public static void main(String[] args) {
        //형변환
        // 형변환이란 정수형-> 실수형
        // 또는 실수형 -> 정수형

        int score = 95;
        System.out.println(score);
        System.out.println((float)score);
        System.out.println((float)score+0.25);


    }
}
